# ============== 完整系统整合 ==============

class LayeredVisualizationSystem:
    """
    分层可视化系统：三层协同工作
    """
    
    def __init__(self):
        self.semantic_analyzer = SemanticAnalyzer()
        self.composer = ComponentComposer()
        self.renderer = AtomicRenderer()
    
    async def visualize(self, natural_language: str) -> str:
        """
        完整流程：自然语言 → SVG
        
        步骤：
        1. 顶层：语义理解
        2. 中层：组合决策
        3. 底层：原子渲染
        """
        print("=" * 60)
        print("🌱 分层抽象可视化系统")
        print("=" * 60)
        
        # 第一层：语义理解
        print("\n【第一层：语义理解】")
        print(f"输入：{natural_language}")
        semantic_graph = await self.semantic_analyzer.analyze(natural_language)
        print(f"语义图类型：{semantic_graph['type']}")
        print(f"实体数量：{len(semantic_graph['entities'])}")
        print(f"关系数量：{len(semantic_graph['relations'])}")
        
        # 第二层：组合决策
        print("\n【第二层：组合决策】")
        composition = await self.composer.compose(semantic_graph)
        print(f"元素数量：{len(composition['elements'])}")
        
        # 第三层：原子渲染
        print("\n【第三层：原子渲染】")
        svg = self.renderer.render(composition)
        print(f"SVG大小：{len(svg)} 字节")
        
        print("\n" + "=" * 60)
        print("✅ 可视化生成完成！")
        print("=" * 60)
        
        return svg


